#include "3-calc.h"

/**
 * op_add - op to add two numbers
 * @a: num1
 * @b: num 2
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substraction of num
 * @a: num1
 * @b: num 2
 *
 * Return: rest
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - op to multiplie two nums
 * @a: num1
 * @b: num 2
 *
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div two nums
 * @a: num1
 * @b: num 2
 *
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - op to add two numbers
 * @a: num1
 * @b: num 2
 *
 * Return: sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
